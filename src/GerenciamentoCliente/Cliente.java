
package GerenciamentoCliente;

public class Cliente 
{
    private String nome;
    private String cpf;
    private String npassagem;
    
    
    
    
    
    
    public String getNome() 
    {
        return nome;
    }

    public void setNome(String nome) 
    {
        this.nome = nome;
    }

    public String getCpf() 
    {
        return cpf;
    }

    public void setCpf(String cpf) 
    {
        this.cpf = cpf;
    }

    public String getNpassagem() 
    {
        return npassagem;
    }

    public void setNpassagem(String npassagem) 
    {
        this.npassagem = npassagem;
    }
    
}
